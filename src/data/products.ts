function buildProduct(seed: ProductSeed): Product {
	const primaryPet = seed.petTypes[0] ?? "dog";
	const inlineCount = Math.min(8, Math.max(4, seed.reviewCountInline ?? 4));
	return {
		id: seed.id,
		slug: seed.slug,
		name: seed.name,
		brand: seed.brand,
		category: seed.category,
		subcategory: seed.subcategory,
		description: seed.description,
		petTypes: seed.petTypes,
		lifestages: seed.lifestages,
		price: seed.price,
		compareAtPrice: seed.compareAtPrice,
		images: seed.images,
		specs: seed.specs,
		ingredients: seed.ingredients,
		rating: seed.rating,
		reviewCount: seed.reviewCount,
		reviews: buildReviews(inlineCount, primaryPet, seed.rating),
		vetNote: seed.vetNote,
		vetRecommended: seed.vetRecommended,
		subscriptionEligible: seed.subscriptionEligible,
		frequentlyBoughtTogether: seed.frequentlyBoughtTogether,
		stockStatus: seed.stockStatus ?? "in_stock",
		createdAt: seed.createdAt,
	};
}

export const products: Product[] = seeds.map(buildProduct);

export function getProductById(id: string): Product | undefined {
	return products.find((p) => p.id === id);
}

export function getProductBySlug(slug: string): Product | undefined {
	return products.find((p) => p.slug === slug);
}
