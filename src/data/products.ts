let reviewSeed = 1;
function buildReviews(
	count: number,
	petType: PetSpecies,
	baseRating: number,
): Review[] {
	const reviews: Review[] = [];
	for (let i = 0; i < count; i++) {
		const positive = i < count - 1 || baseRating >= 4.5;
		const pool = positive ? POS_REVIEWS : MIXED_REVIEWS;
		const pick = pool[(i + reviewSeed) % pool.length];
		const rating = positive ? (baseRating >= 4.6 ? 5 : 4) : 3;
		const month = ((i + reviewSeed) % 12) + 1;
		const day = ((i * 7 + reviewSeed) % 27) + 1;
		reviews.push({
			id: `r-${reviewSeed}-${i}`,
			author: REVIEW_AUTHORS[(i + reviewSeed) % REVIEW_AUTHORS.length],
			petType,
			rating,
			title: pick.title,
			body: pick.body,
			date: `2025-${String(month).padStart(2, "0")}-${String(day).padStart(2, "0")}`,
		});
	}
	reviewSeed += 5;
	return reviews;
}

